export function somar(num1, num2) {
    const soma = num1 + num2;
    return typeof soma === 'number' ? soma : NaN;
}

export function dividir(num1, num2) {
    const divisao = num1 / num2;
    return typeof divisao === 'number' ? divisao : NaN;
}

export function subtrair(num1, num2) {
    const subtracao = num1 - num2;
    return typeof subtracao === 'number' ? subtracao : NaN;
}

export function multiplicar(num1, num2) {
    const multiplicacao = num1 - num2;
    return typeof multiplicacao === 'number' ? multiplicacao : NaN;
}

